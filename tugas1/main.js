function sapaUser(nama) {
    console.log("Halo, " + nama + "! Selamat datang!");
}


sapaUser("Andi"); // Output: "Halo, Andi! Selamat datang!"
sapaUser("Budi"); // Output: "Halo, Budi! Selamat datang!"


// Penjelasan:

//Fungsi sapaUser menerima satu parameter bernama nama.
//Di dalam fungsi, kita menggunakan console.log untuk menampilkan pesan sapaan yang memuat nama tersebut.
//+ digunakan untuk menggabungkan (concatenate) string, sehingga menghasilkan kalimat sapaan lengkap.

//Ketika fungsi ini dipanggil dengan nama tertentu, seperti "Andi" atau "Budi", maka akan menampilkan output sapaan di console.
